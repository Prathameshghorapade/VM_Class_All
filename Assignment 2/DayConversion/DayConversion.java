class DayConversion{

public static void main(String args[]){


   int TotalDays = 400;

   int years = TotalDays / 365;

   int weeks = (TotalDays% 365)/7;

   int days = TotalDays % 7;


 System.out.println("Years: "+years);

 System.out.println("Weeks: "+weeks);

 System.out.println("Days: "+days);




 


}




}